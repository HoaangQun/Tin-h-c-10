#tính diện tích và chu vi hình chữ nhật
r,d = map(float, input("Nhập vào chiều dài/rộng hcn: ").split())
print( f"Chu vi hình chữ nhật = {(r+d)*2}")
print( f"Diện tích hình chữ nhật = {r*d}")

#tính tổng bình phương
a,b = map(float, input("Nhập vào 2 số: ").split())
print( f"Tổng bình phương 2 số = {a**2+b**2}")